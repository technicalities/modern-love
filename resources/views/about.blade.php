<h2>About modern-love</h2>

<p>This site is, or will be, a visual guide to the modern explosion of romantic and sexual identities.</p>

<h2>Intended Features</h2>

<ol>

<li> Skeleton: sliders, explanations</li>
<li> A string representation of your settings.</li>
<li> Graphing any 3 of the seven dimensions.</li>
<li> Animations.</li>
<li> Allowing dynamic identities to be represented.</li>
<li> Optional survey; submit your answers and plot geography.</li>
